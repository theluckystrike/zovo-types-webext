/**
 * @zovo/types-monorepo - Blog Post Generator
 * Auto-generates "What's New" blog posts per browser release
 */

const fs = require('fs');
const path = require('path');

const DIFF_DIR = path.join(__dirname, '../diff-reports');
const BLOG_DIR = path.join(__dirname, '../blog-posts');
const ROOT_DIR = path.join(__dirname, '..');

if (!fs.existsSync(BLOG_DIR)) {
  fs.mkdirSync(BLOG_DIR, { recursive: true });
}

/**
 * Generate blog posts for each browser
 */
function generateBlogPosts() {
  console.log('📝 Generating "What\'s New" Blog Posts\n');
  console.log('═'.repeat(50));

  const diffPath = path.join(DIFF_DIR, 'combined-diff-report.json');
  
  if (!fs.existsSync(diffPath)) {
    console.log('  ⚠️ No diff data found, run diff generator first');
    return;
  }
  
  const diffs = require(diffPath);
  
  for (const [browser, diff] of Object.entries(diffs)) {
    console.log(`  → Generating ${browser} blog post...`);
    const blogPost = generateBlogPost(browser, diff);
    
    const filename = `${browser}-whats-new-${diff.toVersion}.md`;
    const filepath = path.join(BLOG_DIR, filename);
    fs.writeFileSync(filepath, blogPost);
    
    console.log(`     ✅ Saved: ${filename}`);
  }

  // Generate index page
  generateBlogIndex(diffs);

  console.log('\n' + '═'.repeat(50));
  console.log('✅ Blog Posts Generated');
  console.log(`   Saved to: ${BLOG_DIR}`);
  console.log('═'.repeat(50));
}

function generateBlogPost(browser, diff) {
  const capitalizeBrowser = capitalize(browser);
  const date = new Date(diff.generated).toLocaleDateString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric'
  });

  let post = `# What's New in ${capitalizeBrowser} Extensions - ${diff.toVersion}\n\n`;
  post += `*Release Date: ${date}*\n\n`;
  
  post += `---\n\n`;
  
  post += `## Overview\n\n`;
  
  const totalChanges = diff.added.length + diff.removed.length + diff.modified.length;
  post += `This release brings **${totalChanges}** API changes to ${capitalizeBrowser} browser extensions.\n\n`;
  
  post += `| Change Type | Count |\n`;
  post += `|-------------|-------|\n`;
  post += `| 🆕 Added    | ${diff.added.length} |\n`;
  post += `| 🔥 Removed  | ${diff.removed.length} |\n`;
  post += `| ✏️ Modified  | ${diff.modified.length} |\n\n`;
  
  // New APIs section
  if (diff.added.length > 0) {
    post += `## 🆕 New APIs\n\n`;
    
    const byCategory = categorizeAPIs(diff.added);
    for (const [category, apis] of Object.entries(byCategory)) {
      post += `### ${category}\n\n`;
      apis.forEach(api => {
        post += `#### \`chrome.${api.namespace}\`\n\n`;
        post += `${api.description || 'New API endpoint for extensions.'}\n\n`;
        
        // Add usage example
        post += '````javascript\n';
        post += `// Example usage\n`;
        post += `chrome.${api.namespace}`;
        
        const firstFunc = api.functions?.[0];
        if (firstFunc) {
          post += `.${firstFunc.name}(${firstFunc.parameters.map(p => p.name).join(', ')})`;
        }
        post += '\n';
        post += '  .then(result => console.log(result));\n';
        post += '````\n\n';
      });
    }
  }
  
  // Removed APIs warning
  if (diff.removed.length > 0) {
    post += `## ⚠️ Breaking Changes\n\n`;
    post += `The following APIs have been **removed** in this release. `;
    post += `Please update your extensions to avoid breaking changes.\n\n`;
    
    diff.removed.forEach(api => {
      post += `- ~~\`chrome.${api.namespace}\`~~\n`;
    });
    post += '\n';
  }
  
  // Modified APIs
  if (diff.modified.length > 0) {
    post += `## ✏️ Modified APIs\n\n`;
    
    diff.modified.forEach(api => {
      post += `### \`chrome.${api.namespace}\`\n\n`;
      
      const added = api.changes.filter(c => c.change === 'added');
      const removed = api.changes.filter(c => c.change === 'removed');
      
      if (added.length > 0) {
        post += `**New:**\n`;
        added.forEach(c => {
          post += `- +${c.type} \`${c.name}\`\n`;
        });
        post += '\n';
      }
      
      if (removed.length > 0) {
        post += `**Removed:**\n`;
        removed.forEach(c => {
          post += `- -${c.type} \`${c.name}\`\n`;
        });
        post += '\n';
      }
    });
  }
  
  // Migration guide placeholder
  post += `---\n\n`;
  post += `## Migration Guide\n\n`;
  post += `For detailed migration instructions, check our [migration guide](../docs/migration-guide.md).\n\n`;
  
  // Footer
  post += `---\n\n`;
  post += `*This post was auto-generated by @zovo/types-webext*\n`;
  
  return post;
}

function generateBlogIndex(diffs) {
  let index = `# Browser Extension API Updates\n\n`;
  index += `Welcome to the browser extension API update blog. Here you'll find the latest changes to browser extension APIs across Chrome, Firefox, Safari, and Edge.\n\n`;
  
  index += `## Latest Updates\n\n`;
  
  for (const [browser, diff] of Object.entries(diffs)) {
    const date = new Date(diff.generated).toLocaleDateString('en-US', {
      year: 'numeric',
      month: 'long',
      day: 'numeric'
    });
    
    index += `### ${capitalize(browser)} ${diff.toVersion}\n`;
    index += `*${date}*\n\n`;
    index += `- ${diff.added.length} new APIs\n`;
    index += `- ${diff.removed.length} removed\n`;
    index += `- ${diff.modified.length} modified\n\n`;
    index += `[Read more →](./${browser}-whats-new-${diff.toVersion}.md)\n\n`;
  }
  
  index += `---\n\n`;
  index += `*Subscribe to get notified about new API updates*\n`;
  
  fs.writeFileSync(path.join(BLOG_DIR, 'index.md'), index);
}

function categorizeAPIs(apis) {
  const categories = {
    'Tabs & Windows': ['tabs', 'windows', 'tabGroups'],
    'Storage': ['storage', 'storageSync', 'storageManaged'],
    'Network': ['webRequest', 'declarativeNetRequest', 'proxy'],
    'Content': ['scripting', 'contentScripts', 'userScripts'],
    'Browsing': ['bookmarks', 'history', 'downloads', 'cookies'],
    'UI': ['notifications', 'contextMenus', 'commands', 'omnibox'],
    'Identity': ['identity', 'identityPrivate'],
    'Other': []
  };
  
  const result = { ...categories };
  
  apis.forEach(api => {
    let categorized = false;
    const ns = api.namespace.split('.')[0];
    
    for (const [category, keywords] of Object.entries(categories)) {
      if (keywords.includes(ns)) {
        if (!result[category]) result[category] = [];
        result[category].push(api);
        categorized = true;
        break;
      }
    }
    
    if (!categorized) {
      result['Other'].push(api);
    }
  });
  
  // Remove empty categories
  for (const key of Object.keys(result)) {
    if (result[key].length === 0) {
      delete result[key];
    }
  }
  
  return result;
}

function capitalize(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

if (require.main === module) {
  generateBlogPosts();
}

module.exports = { generateBlogPosts };
